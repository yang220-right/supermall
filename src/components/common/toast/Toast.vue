<template>
  <div class="toast" v-show='isShow'>
    <div>{{message}}</div>
  </div>
</template>
<script>
export default {
  name: "Toast",
  data() {
    return { message: "", isShow: false };
  },
  methods:{
    show(message='默认文字',duration=2000){
      // 默认参数 duration = duration || 2000
      this.isShow = true
      this.message = message
      setTimeout(()=>{
        this.isShow = false
        this.message = ''
      },duration)
    }
  }
};
</script>
<style scoped>
.toast{
  position: fixed;
  top: 50%;
  left: 50%;
  transform: translate(-50%,-50%);
  color:#fff;
  background-color: #000;
  opacity: .75;
  padding: 10px 15px;
  z-index: 999;
}
</style>